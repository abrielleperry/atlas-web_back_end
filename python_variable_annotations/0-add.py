#!/usr/bin/env python3
""" Takes two floating numbers as arguments and adds them to return a float.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers.

    Args:
        a (float): first number to addition.
        b (float): second number to addition.

    Returns:
        float: sum of a and b.
    """
    return a + b

#!/usr/bin/env python3
""" function concat takes str1 and str2 as args and returns a concat string
"""


def concat(str1: str, str2: str) -> str:
    """ concats two string args into a single string

    Args:
    str1 (str): first string to concat
    str2 (str): second string to concat

    Returns:
    str: a single string
    """
    return str1 + str2
